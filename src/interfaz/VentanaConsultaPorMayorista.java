    @Override
    public void update(Observable o, Object obj) {
        this.mayoristasAPantalla();
        if (cboxMayoristas.getSelectedItem() != null) {
            this.datosAPantalla();
        }
    }

    /**
     * Cargo la lista de mayoristas ordenada alfabeticamente en el combobox
     */
    public void mayoristasAPantalla() {
        Mayorista m = (Mayorista) cboxMayoristas.getSelectedItem();
        cboxMayoristas.setModel(new javax.swing.DefaultComboBoxModel<>(this.getModelo().getListaMayoristas().toArray()));
        if (m != null) {
            cboxMayoristas.setSelectedItem(m);
        }
    }

    /**
     * Muesto los datos del mayorista seleccionado en pantalla
     */
    public void datosAPantalla() {
        Mayorista m = (Mayorista) cboxMayoristas.getSelectedItem();
        
        txtRUT.setText(String.valueOf(m.getRUT()));
        txtNombre.setText(m.getNombre());
        txtDireccion.setText(m.getDireccion());
        
        lstProdsOfrece.setListData(m.getProdsQueOfrece().toArray());
    }

