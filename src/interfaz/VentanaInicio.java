    private void btnCargarProdsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarProdsActionPerformed
        Sistema s = new Sistema();
        int contadorCargas = 0;
        ArchivoLectura arch = new ArchivoLectura("ImagenesOblig/productos.txt");
        while (arch.hayMasLineas()) {
            String linea = arch.linea();
            String[] datos = linea.split("@");

            if (Auxiliar.verificarLinea(datos[0], 1, 50)) {
                if (Auxiliar.verificarLinea(datos[1], 1, 100)) {
                    boolean tipoValido = datos[2].equalsIgnoreCase("Fruta")
                            || datos[2].equalsIgnoreCase("Verdura");

                    boolean formaValida = datos[3].equalsIgnoreCase("Unidad")
                            || datos[3].equalsIgnoreCase("Kilogramo");
                    if (tipoValido && formaValida) {
                        ImageIcon archivoIcono = new ImageIcon("ImagenesOblig/" + datos[4]);
                        //Se consigue la imagen y se hace el resize
                        Image iconoAImagen = ((ImageIcon) archivoIcono).getImage();
                        Image imagenReescalada = iconoAImagen.getScaledInstance(60, 60, Image.SCALE_SMOOTH);

                        ImageIcon image = new ImageIcon(imagenReescalada);

                        Producto p = new Producto(datos[0], datos[1], datos[2], datos[3], image);
                        if (s.agregarProductoALista(p)) {
                            contadorCargas++;
                        }
                    }
                }
            }
        }
        arch.cerrar();
        showMessageDialog(this, "Carga completa: se han cargado " + contadorCargas + " productos al sistema.", "Productos registrados.", INFORMATION_MESSAGE);

        VentanaPrincipal vPrincipal = new VentanaPrincipal(s);
        vPrincipal.setVisible(true);

        this.dispose();
    }//GEN-LAST:event_btnCargarProdsActionPerformed


